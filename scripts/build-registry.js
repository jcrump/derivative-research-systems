#!/usr/bin/env node
/**
 * DRS Blog Registry Builder
 *
 * If you maintain posts as JSON files in /blog/posts/*.json
 * this script regenerates blog/blog-registry.js from them.
 *
 * JSON post format:
 * {
 *   "slug": "my-post-slug",
 *   "title": "Post Title",
 *   "excerpt": "One sentence summary.",
 *   "category": "Architecture",
 *   "date": "2026-06-24",
 *   "readTime": "6 min read",
 *   "tags": ["Kafka", "Java"]
 * }
 *
 * Usage:
 *   npm run build:registry
 *
 * The Blog Admin (admin/index.html) exports blog-registry.js directly,
 * so this script is only needed if you prefer maintaining posts as JSON
 * source files in version control instead of using the Admin UI export.
 */

const fs   = require('fs');
const path = require('path');

const POSTS_DIR    = path.resolve(__dirname, '../blog/posts');
const REGISTRY_OUT = path.resolve(__dirname, '../blog/blog-registry.js');

function log(msg, color = '\x1b[32m') {
  console.log(`${color}%s\x1b[0m`, msg);
}

if (!fs.existsSync(POSTS_DIR)) {
  log('  ℹ No blog/posts/ directory found — nothing to do.', '\x1b[33m');
  log('  Create JSON files in blog/posts/ or use the Blog Admin to export.\n', '\x1b[33m');
  process.exit(0);
}

const files = fs.readdirSync(POSTS_DIR).filter(f => f.endsWith('.json'));

if (!files.length) {
  log('  ℹ No .json post files found in blog/posts/ — nothing to regenerate.\n', '\x1b[33m');
  process.exit(0);
}

const posts = [];
for (const file of files) {
  try {
    const raw  = fs.readFileSync(path.join(POSTS_DIR, file), 'utf8');
    const post = JSON.parse(raw);
    if (!post.slug || !post.title) {
      log(`  ⚠  Skipping ${file} — missing slug or title`, '\x1b[33m');
      continue;
    }
    posts.push(post);
    log(`  ✓ ${file}`);
  } catch (e) {
    log(`  ✗ Error parsing ${file}: ${e.message}`, '\x1b[31m');
  }
}

// Sort newest first
posts.sort((a, b) => (b.date || '').localeCompare(a.date || ''));

const entries = posts.map(p => `  {
    slug: ${JSON.stringify(p.slug)},
    title: ${JSON.stringify(p.title)},
    excerpt: ${JSON.stringify(p.excerpt || '')},
    category: ${JSON.stringify(p.category || '')},
    date: ${JSON.stringify(p.date || '')},
    readTime: ${JSON.stringify(p.readTime || '5 min read')},
    tags: ${JSON.stringify(p.tags || [])}
  }`).join(',\n');

const output = `// DRS Blog Registry
// Generated by scripts/build-registry.js on ${new Date().toISOString()}
// DO NOT edit by hand — regenerate with: npm run build:registry
var DRS_BLOG_POSTS = [
${entries}
];
`;

fs.writeFileSync(REGISTRY_OUT, output, 'utf8');
log(`\n✅ blog-registry.js updated — ${posts.length} post(s) indexed.\n`);
